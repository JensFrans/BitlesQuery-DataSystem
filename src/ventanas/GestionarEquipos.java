    private void jButton_mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_mostrarActionPerformed

        String seleccion = cmb_estatus.getSelectedItem().toString();
        String query = "";
        
        model.setRowCount(0);
        model.setColumnCount(0);
        
        try {
            Connection cn = Conexion.conectar();
            
            if (seleccion.equalsIgnoreCase("Todos")) {
                query = "select id_materia, id_alumno, tipo_materia, calificacion, status from materias";
            } else {
                query = "select id_materia, id_alumno, tipo_materia, calificacion, status from materias where tipo_materia = '" + seleccion + "'";
            }
            
            PreparedStatement pst = cn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            
            jTable_materias = new JTable(model);
            jScrollPane_materias.setViewportView(jTable_materias);
            
            model.addColumn("ID_Materia");
            model.addColumn("ID_Alumno");
            model.addColumn("Materia");
            model.addColumn("Calificacion");
            model.addColumn("Estatus");
            
            while (rs.next()) {
                Object [] fila = new Object[5];
                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                
                model.addRow(fila);
            }
            cn.close();
            
            
            
        } catch (SQLException e) {
            System.err.println("Error al llenar la tabla equipos registrados" + e);
        }
        
    ObtenerDatosTabla();
    
    }//GEN-LAST:event_jButton_mostrarActionPerformed

