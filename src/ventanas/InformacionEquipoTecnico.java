    private void jButton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarActionPerformed

     String status, comentarios, tecnico;
     
     status = cmb_status.getSelectedItem().toString();
     comentarios = jTextPane_comentariosTecnico.getText();
     tecnico = user;
     
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "update materias set status=?, comentarios_tecnicos=?, revision_tecnica_de=? where id_materia = '" + IDequipo + "'");
            
            pst.setString(1, status);
            pst.setString(2, comentarios);
            pst.setString(3, tecnico);
            
            pst.executeUpdate();
            cn.close();
            
            JOptionPane.showMessageDialog(null, "Actualizacion exitosa.");
            this.dispose();
            
        } catch (SQLException e) {
            System.err.println("Error al actualizar materia");
            JOptionPane.showMessageDialog(null, "¡¡Error al actulizar materia!!, contacte al administrador.");
        }
        
    }//GEN-LAST:event_jButton_ActualizarActionPerformed

