    public InformacionEquipoTecnico() {
        initComponents();
        user = Login.user;
        IDequipo = GestionarEquipos.IDequipo_update;
        IDcliente_update = GestionarClientes.IDcliente_update;
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select nombre_alumno from alumnoss where id_alumno = '" + IDcliente_update + "'");
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                nom_alumno = rs.getString("nombre_alumno");
            }
            
        } catch (Exception e) {
            System.err.println("Error al consultar nombre...");
        }
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select * from materias where id_materia = '" + IDequipo + "'");
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                cmb_materia.setSelectedItem(rs.getString("tipo_materia"));
                cmb_status.setSelectedItem(rs.getString("status"));
                
                txt_calificacion.setText(rs.getString("calificacion"));
                txt_maestro.setText(rs.getString("maestro"));
                txt_ultima_Modificacion.setText(rs.getString("ultima_modificacion"));
                
                String dia = "", mes = "", annio = "";
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                annio = rs.getString("annio_ingreso");
                txt_fecha.setText(dia + " del " + mes + " del "+ annio);
                
                jTextPane_observaciones.setText(rs.getString("Observaciones"));
                jTextPane_comentariosTecnico.setText(rs.getString("comentarios_tecnicos"));
                jLabel_revisionTecnicaDe.setText("De " + rs.getString("revision_tecnica_de"));
                
                
                
            }
            
        } catch (Exception e) {
            System.err.println("Error al consultar la informacion de materia"); 
        }
        
        setTitle("Materia registrada con id " + IDequipo + " - Sesion de "+ user);
        setSize(800, 600);
        setResizable(false);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon(getClass().getResource("/images/gui_imm.png"));
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),
                jLabel_Wallpaper.getHeight(), Image.SCALE_AREA_AVERAGING));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
       
        txt_NombreAlumno.setText(nom_alumno);
    }

