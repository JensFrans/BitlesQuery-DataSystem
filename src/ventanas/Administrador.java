    public Administrador() {
        initComponents();
        tipoFuente = new Fuentes();
        user = Login.user;
        sesion_usuario = 1;

        setSize(800, 600);
        setResizable(false);
        setTitle("Administrador - Sesion de " + user);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon(getClass().getResource("/images/gui3.png"));
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),
             jLabel_Wallpaper.getHeight(), Image.SCALE_AREA_AVERAGING));
        
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
            "select nombre_usuario from usuarios where username = '" + user + "'");
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                
                
                nombre_usuario = rs.getString("nombre_usuario");
                jLabel_footer.setFont(tipoFuente.fuente(tipoFuente.BPR, 0, 12));
                jLabel_Nombreusuario.setFont(tipoFuente.fuente(tipoFuente.BPR, 1, 30));
                jLabel_Nombreusuario.setText(nombre_usuario);
                
            }
            
        } catch (Exception e) {
            System.err.println("Error de conexion desde la interfaz de Administrador");
        }
        
        
    }
    
