    private void jButton_ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ImprimirActionPerformed
        
        Document documento = new Document();
        
        try {
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/Lista_de_Almunos_Registrados.pdf"));
            
            com.itextpdf.text.Image header = com.itextpdf.text.Image.getInstance("src/images/BannerPDF.jpg");
            header.scaleToFit(600, 1000);
            header.setAlignment(Chunk.ALIGN_CENTER);
            
            Paragraph parrafo = new Paragraph();
            parrafo.setAlignment(Paragraph.ALIGN_CENTER);
            parrafo.add("Lista de Alumnos Registrados \n \n");
            parrafo.setFont(FontFactory.getFont("Tahoma", 18, Font.BOLD, BaseColor.DARK_GRAY));
            
            documento.open();
            documento.add(header);
            documento.add(parrafo);
            
            PdfPTable tabla = new PdfPTable(6);
            tabla.setWidthPercentage(112f);
            float[] ancho = {2.55f,10.55f,5.50f,13.10f,12.90f,6.90f};//7.60f
            tabla.setWidths(ancho);
            
            tabla.addCell("ID");
            tabla.addCell("Nombre");
            tabla.addCell("C. I.");
            tabla.addCell("Mención");
            tabla.addCell("E-mail");
            tabla.addCell("Teléfono");
            /*tabla.addCell("Dirección");*/

            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "select * from alumnoss");
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                    do {                        
                        
                        tabla.addCell(rs.getString(1));
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(8));
                        tabla.addCell(rs.getString(7));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                        /*tabla.addCell(rs.getString(5));*/
                        
                        
                    } while (rs.next());
                    documento.add(tabla);
                    
                }
                
            } catch (SQLException e) {
                System.err.println("Error al cargar lista de alumnos.");
            }
            
            documento.close();
            JOptionPane.showMessageDialog(null, "Reporte de alumnos creado correctamente.");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al generar pdf...");
        }
        
    }//GEN-LAST:event_jButton_ImprimirActionPerformed

