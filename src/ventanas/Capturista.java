    public Capturista() {
        initComponents();
        tipoFuente = new Fuentes();
        user = Login.user;
        sesion_usuario = Administrador.sesion_usuario;
        
        setSize(800, 600);
        setResizable(false);
        setTitle("Coordinador - Sesion de " + user);
        setLocationRelativeTo(null);
        
        if (sesion_usuario == 1) {
            setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
            
        } else {
            setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        }
        
        ImageIcon wallpaper = new ImageIcon(getClass().getResource("/images/gui_coo.png"));
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),
             jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select nombre_usuario from usuarios where username = '" + user + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                
                nombre_usuario = rs.getString("nombre_usuario");
                jLabel_footer.setFont(tipoFuente.fuente(tipoFuente.BPR, 0, 12));
                jLabel_user.setFont(tipoFuente.fuente(tipoFuente.BPR, 1, 30));
                jLabel_user.setText(nombre_usuario);
                
            }
        } catch (SQLException e) {
            System.out.println("Error al consultar nombre de coordinador");
        }
    }
    
